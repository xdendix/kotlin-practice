package basic.kotlin.practice

fun main() {

    val a = 100
    val b = 300

    // contoh Operasi Perbandingan (lebih dari >)
    val result1: Boolean = a > b
    println(result1)

    // contoh Operasi Perbandingan (kurang dari <)
    val result2: Boolean = a < b
    println(result2)

    // contoh Operasi Perbandingan (lebih dari sama dengan >=)
    val result3: Boolean = a >= b
    println(result3)

    // contoh Operasi Perbandingan (kurang dari sama dengan <=)
    val result4: Boolean = a <= b
    println(result4)

    // contoh Operasi Perbandingan (sama dengan ==)
    val result5: Boolean = a == b
    println(result5)

    // contoh Operasi Perbandingan (tidak sama dengan !=)
    val result6: Boolean = a != b
    println(result6)
}