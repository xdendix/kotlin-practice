package basic.kotlin.practice

fun main() {

    val nilaiAkhir = 90

    if (nilaiAkhir >= 90) {
        println("Great")
    } else if (nilaiAkhir >= 80) {
        println("Good Job")
    } else if (nilaiAkhir >= 70) {
        println("Good")
    } else if (nilaiAkhir >= 60) {
        println("Not Bad")
    } else {
        println("Try Again!")
    }
}